                                                    4.generalne reguly
                                                    ------------------
- ostateczna cena wejsciowki zostala ustalona na 30 zlotych. ladne
  dziewuszki wchodza za darmo. nie przewidujemy zadnych znizek - zbyt
  wiele z tym balaganu, a pozniej wiecej obrazonych, niz zadowolonych;

- identyfiktora nie da sie skserowac (da sie fizycznie, ale to nic nie
  da) - "legalni" partowicze dostana tez specjalna pieczatke na reke,
  ktora bedzie spawdzana bardziej niz papierek na piersi (tu mozecie
  miec cokolwiek).  w waszym zatem interesie jest jej zachowanie
  do konca party...  zamazanie lub starcie powoduje koniecznosc
  szybkiego ponowienia wplaty na rzecz wejscia... nie liczcie na
  darmowe wejscie koczujac pod drzwiami.

- piekny zwyczaj, zapoczatkowany wlasnie u nas bedzie kontynuowany:
  wszycy dresiarze, lokciowcy, dyskomuly i tego typu srodowiska sa
  bardzo niemile widziane na party, a jesli juz ktos sie uprze, to i
  dla niego cena nie wzrosla - 50 plz lub przebranie na zewnatrz...

- minimalna nagroda to coô wiecej, niû zwrot kosztów wejôcia :) to
  takze mialo miesce jedynie u nas i nie zamierzamy tego zmieniac!

- nagrody beda jak zwykle fundowane z dwoch zrodel - pierwsze to
  wplywy ze sprzedanych wejsciowek, a druga to rzeczowe upominki od
  sponsorow.  ponownie otrzymalismy dotacje ze strony politechniki
  koszalinskiej na rozkrecenie atrakcyjnosci interesu.  wiekszy koszt
  wejscia oznacza takze nieporownywalnie wieksze nagrody pieniezne.
  prosimy jednak o nie nastawianie sie na materialny aspekt nagrod -
  niech maja one raczej charakter prestizowy...

- podczas nagradzania nie stosujemy dziwnej zasady znanej z innych
  parties - "zwyciezca bierze wszytko" - pule przeznaczona na
  dany konkurs dzielimy posrod trzech zwyciezcow;

- glosowanie odbedzie sie przy uzyciu dyskietek, na ktorych
  znajdziecie specjalny plik do wypelnienia. na partyplace bedzie
  udostepniona maszyna dla ludu, przy pomocy ktorej bedziecie mogli
  spokojnie zaglosowac. obok bedzie czuwal bezstronny organizator
  zniechecajac was do machlojek =)

- dziekujemy wam za niezniszczenie (relatywnie - ktos tam cwiczyl
  karate, zdaniem kierownika sali =) ubikacji na poprzednim party -
  sprawcie sie tak ponownie.  ciezko bedzie wymyslec cos ciekawszego
  od zaklejenia kloaki plakatami z disco polo, ale postaramy sie. poza
  tym bedzie mozna sie wyzyc tworczo na scenie...

