obok  siebie  (z  tym,  ze autobusowy blizej pierwszego skrzyzowania).
Jesli  ktos  jest  uczulony na obicia siedzen w autobusach komunikacji
miejskiej,  to  tuz obok dworca znajduje sie postoj taksowek.  kurs na
partyplace  kosztuje  okolo  6  zlotych,  choc  moze  to byc mniej lub
wiecej.  Mafia taksowkowa oficjalnie nie wystepuje.

