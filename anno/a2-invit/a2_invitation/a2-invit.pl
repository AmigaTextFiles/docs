                                                     2. kiedy i gdzie?
                                                     -----------------
        Astrosyn`2 odbedzie sie ostatecznie w zimowo/wiosenny weekend,
20-21  marca  1999 roku w hali sportowej znajdujacej sie na terenie PK
przy   ulicy   raclawickiej  15/17  w  Koszalinie,  czyli  tam,  gdzie
poprzednio.   jest  to  pomieszczenie na tyle spore, ûe zdola wessac i
zaaklimatyzowac  wszystkich, ktorzy zechca sie pojawic, a jednoczesnie
nie  bedziemy  lazic  po  sobie  nawzajem.   jest  to  spory  kompleks
posiadajacy  wlasne zaplecze sprzetowe i sporo salek lekcyjnych, ktore
postaramy  sie  zaadoptowac  dla  potrzeb  wszelkiego  rodzaju roomow.
lokalizacja  jest  raczej bezproblemowa, tzn.  okolo 15-20 minut drogi
od  dworca  PKP/PKS  i  w  centrum miasta.  Dokladniejszych informacji
szukaj  dalej.   wokolo w promieniu 700m nie ma siedzib ludzkich, wiec
odpadaja   wszelkie  skargi  o  halas  etc.   Jak  fajnie  zostalo  to
pomyslane, zobaczycie juû na miejscu...


